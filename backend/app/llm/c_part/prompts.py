def format_topic_classifier_prompt(question: str, chat_history=None) -> str:
    """
    Args:
        question: 사용자 질문
        chat_history: 직전 대화 목록(선택). [{"role","content"}, ...] 또는
                      role/content 속성을 가진 객체 리스트.
                      넘기지 않으면 맥락 없이 질문만으로 판단합니다.
    """
    chat_context = "(없음)"
 
    if chat_history:
        recent = list(chat_history)[-4:]  # 최근 2턴 정도만
        lines = []
        for message in recent:
            role = getattr(message, "role", None) or (
                message.get("role") if isinstance(message, dict) else None
            )
            content = getattr(message, "content", None) or (
                message.get("content") if isinstance(message, dict) else ""
            )
            speaker = "사용자" if role == "user" else "챗봇"
            text = str(content).replace("\n", " ").strip()
            if len(text) > 200:
                text = f"{text[:200]}..."
            if text:
                lines.append(f"{speaker}: {text}")
        if lines:
            chat_context = "\n".join(lines)
 
    return TOPIC_CLASSIFIER_PROMPT.format(
        question=question,
        chat_context=chat_context,
    )
 
